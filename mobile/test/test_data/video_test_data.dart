import 'package:openvine/models/video_event.dart';

/// Creates a test VideoEvent with customizable properties
VideoEvent createTestVideoEvent({
  String id = 'test_video_123',
  String pubkey = 'test_pubkey_456',
  int createdAt = 1704067200, // 2024-01-01
  String content = 'Test video content',
  String? title,
  String? videoUrl = 'https://example.com/video.mp4',
  String? thumbnailUrl,
  int? duration,
  String? dimensions,
  String? mimeType,
  String? sha256,
  int? fileSize,
  List<String> hashtags = const [],
  String? publishedAt,
  Map<String, String> rawTags = const {},
  String? vineId,
  String? group,
  String? altText,
  String? blurhash,
  bool isRepost = false,
  String? reposterId,
  String? reposterPubkey,
  DateTime? repostedAt,
  bool isFlaggedContent = false,
  String? moderationStatus,
  int? originalLoops,
  int? originalLikes,
}) {
  return VideoEvent(
    id: id,
    pubkey: pubkey,
    createdAt: createdAt,
    content: content,
    timestamp: DateTime.fromMillisecondsSinceEpoch(createdAt * 1000),
    title: title,
    videoUrl: videoUrl,
    thumbnailUrl: thumbnailUrl,
    duration: duration,
    dimensions: dimensions,
    mimeType: mimeType,
    sha256: sha256,
    fileSize: fileSize,
    hashtags: hashtags,
    publishedAt: publishedAt,
    rawTags: rawTags,
    vineId: vineId ?? id,
    group: group,
    altText: altText,
    blurhash: blurhash,
    isRepost: isRepost,
    reposterId: reposterId,
    reposterPubkey: reposterPubkey,
    repostedAt: repostedAt,
    isFlaggedContent: isFlaggedContent,
    moderationStatus: moderationStatus,
    originalLoops: originalLoops,
    originalLikes: originalLikes,
  );
}